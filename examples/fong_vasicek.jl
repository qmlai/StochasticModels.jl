using StochasticModels

#-------------------------------------------------------------------------------------------#
#                               IIP Fong Vasicek Model                                      #
#-------------------------------------------------------------------------------------------#

u0    = [0.1, 0.1]
tspan = (0.0, 1.0)

p = (κ₁ = 0.1, 
     κ₂ = 0.1, 
     θ₁ = 0.1, 
     θ₂ = 0.1, 
     η  = 0.1, 
     ρ  = 0.1)

fw = FongVasicekModel(u0, p, tspan)

fwsol = solve(fw, SRA())

