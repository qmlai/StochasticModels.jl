using StochasticModels, BenchmarkTools

#-------------------------------------------------------------------------------------------#
#                                  IIP Gibson Schwartz Model                                #
#-------------------------------------------------------------------------------------------#

u0    = [1., 1.]
tspan = (0.0, 1.0)
p     = (r=0.1, κ=0.43, 
         α=1.0, λ=0.78, 
         σ₁=0.12, σ₂=0.13)


gs = GibsonSchwartzModel(u0, p, tspan)

solve(gs, SRA1())

