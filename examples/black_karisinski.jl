using StochasticModels

#-------------------------------------------------------------------------------------------#
#                                1-D Black Karasinski Model                                 #
#-------------------------------------------------------------------------------------------#

u0     = 1.2
tspan  = (0.0, 1.0)
params = (θ=t->1.0, ϕ=t->0.2, σ=t->0.2) 

bk = BlackKarasinskiModel(u0, params, tspan)

solve(bk, SRIW1())