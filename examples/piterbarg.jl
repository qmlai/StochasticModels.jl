using StochasticModels

#-------------------------------------------------------------------------------------------#
#                                   IIP Piterbaarg Model                                    #
#-------------------------------------------------------------------------------------------#

x0     = [2., 20.]
tspan  = (0.0, 1.0)
params = (σ=t->0.12, 
          β=t->0.12, 
          θ=t->0.12, 
          γ=t->0.12)

pt = PiterbargModel(x0, params, tspan)
solve(pt, SRA1())

