using StochasticModels

#-------------------------------------------------------------------------------------------#
#                                     1-D CIR Model                                         #
#-------------------------------------------------------------------------------------------#

κ = 0.1
θ = 0.1
σ = 0.12

p = (κ=κ, θ=θ, σ=σ)

x0 = 0.1
tspan = (0.0, 1.0)

cir = CoxIngersollRossModel(x0, p, tspan)
cirsol = solve(cir, SRIW1())

#-------------------------------------------------------------------------------------------#
#                                  N-D IIP Diagonal CIR Model                               #
#-------------------------------------------------------------------------------------------#

x0    = [0.1, 0.2]
tspan = (0.0, 1.0)

κ = [0.1 0.1; 0.1 0.1]
θ = [0.1, 0.1]
σ = [0.1, 0.1]
p = (κ=κ, θ=θ, σ=σ)

cir = CoxIngersollRossModel(x0, p, tspan)
cirsol = solve(cir, SRA1())

#-------------------------------------------------------------------------------------------#
#                              N-D Non Diagonal IIP CIR Model                               #
#-------------------------------------------------------------------------------------------#

x0    = [0.1, 0.2]
tspan = (0.0, 1.0)

κ = [0.1 0.1; 0.1 0.1]
θ = [0.1, 0.1]
σ = [0.1 0.1; 0.1 0.1]
p = (κ=κ, θ=θ, σ=σ)

cir = CoxIngersollRossModel(x0, p, tspan)
cirsol = solve(cir, SRA1())

#-------------------------------------------------------------------------------------------#
#                                  N-D IIP Correlated CIR Model                             #
#-------------------------------------------------------------------------------------------#

x0    = [0.1, 0.2]
tspan = (0.0, 1.0)

κ = [0.1 0.1; 0.1 0.1]
θ = [0.1, 0.1]
σ = [0.1 0.1; 0.1 0.1]
Γ = [1.0 0.1; 1.0 0.1]
p = (κ=κ, θ=θ, σ=σ, Γ=Γ)

cir = CoxIngersollRossModel(x0, p, tspan)
cirsol = solve(cir, SRA1())
