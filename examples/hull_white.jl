using StochasticModels

#-------------------------------------------------------------------------------------------#
#                                  1-D Hull White Model                                     #
#-------------------------------------------------------------------------------------------#

x0    = 0.1
tspan = (0.0, 1.0)

κ(t) = 0.1 * sin(t)
θ(t) = 0.1 * cos(t)
σ(t) = 0.12 * tan(t)

p = (κ=κ, θ=θ, σ=σ)

hw    = HullWhiteModel(x0, p, tspan)
hwsol = solve(hw, SRIW1())

#-------------------------------------------------------------------------------------------#
#                              N-D Diagonal Hull White Model                                #
#-------------------------------------------------------------------------------------------#

x0    = [0.1, 2.]
tspan = (0.0, 1.0)

κ(u, t) = u .= [0.1 * sin(t) 0.1 * sin(t); 0.1 * sin(t) 0.1 * sin(t)]
θ(u, t) = u .= [0.1 * sin(t), 0.1 * sin(t)]
σ(u, t) = u .= [0.1 * sin(t), 0.1 * sin(t)]

p = (κ=κ, θ=θ, σ=σ)

hw    = HullWhiteModel(x0, p, :Diagonal, tspan, )
hwsol = solve(hw, SRA1())

#-------------------------------------------------------------------------------------------#
#                              N-D Non Diagonal Hull White Model                            #
#-------------------------------------------------------------------------------------------#

x0    = [0.1, 2.]
tspan = (0.0, 1.0)

κ(u, t) = u .= [0.1 * sin(t) 0.1 * sin(t); 0.1 * sin(t) 0.1 * sin(t)]
θ(u, t) = u .= [0.1 * sin(t), 0.1 * sin(t)]
σ(u, t) = u .= [0.1 * sin(t) 0.1 * sin(t); 0.1 * sin(t) 0.1 * sin(t)]

p = (κ=κ, θ=θ, σ=σ)

hw    = HullWhiteModel(x0, p, :NonDiagonal, tspan)
hwsol = solve(hw, SRA1())
