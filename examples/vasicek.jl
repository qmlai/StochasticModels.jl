using StochasticModels, BenchmarkTools

#-------------------------------------------------------------------------------------------#
#                                      1-D Vasicek Model                                    #
#-------------------------------------------------------------------------------------------#

x0    = 0.1
tspan = (0.0, 1.0)

κ = 0.1 
θ = 0.1 
σ = 0.12

p = (κ=κ, θ=θ, σ=σ)

vk = VasicekModel(x0, p, tspan)
vs = solve(vk, SRIW1())

#-------------------------------------------------------------------------------------------#
#                               N-D Diagonal Vasicek Model                                  #
#-------------------------------------------------------------------------------------------#

x0    = [0.1, 2.]
tspan = (0.0, 1.0)

κ = [0.1  0.1 ; 0.1  0.1 ]
θ = [0.1 , 0.1 ]
σ = [0.1 , 0.1 ]

p = (κ=κ, θ=θ, σ=σ)

vk = VasicekModel(x0, p, tspan)
vs = solve(vk, SRA1())

