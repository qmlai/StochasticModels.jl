using StochasticModels 

#-------------------------------------------------------------------------------------------#
#                              1-D Clewlow Strickland Model                                 #
#-------------------------------------------------------------------------------------------#

x0    = 0.1
tspan = (0.0, 1.0)

σ = 0.1
α = 0.1
τ = 0.12

p = (α=α, σ=σ, τ=τ)

cs = ClewlowStricklandModel(x0, p, tspan)
solve(cs, SRIW1())

