using StochasticModels 

#-------------------------------------------------------------------------------------------#
#                                   1-D GARCH Model Model                                   #
#-------------------------------------------------------------------------------------------#

u0    = 1.0
tspan = (0.0, 1.0)
p     = (θ=1., ω=0.1, ξ=2.)

gch = GARCHModel(u0, p, tspan)

solve(gch, SRIW1())